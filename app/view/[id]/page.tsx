export default async function ViewPage({ params }: Props) {
  const { id } = await params
  const admin = createAdminClient()

  const { data: gen } = await admin
    .from('generations')
    .select('tool_type, output_data, status, locale, created_at, user_id, client_id, invoice_status, accepted_at')
    .eq('id', id)
    .single()

  if (!gen || gen.status !== 'completed' || !gen.output_data) notFound()

  const [{ data: company }] = await Promise.all([
    admin
      .from('company_profiles')
      .select('company_name, contact_name, email, phone, address, logo_url')
      .eq('user_id', gen.user_id)
      .single(),
  ])

  await admin.from('document_events').insert({
    generation_id: id,
    user_id: gen.user_id,
    client_id: (gen as { client_id?: string | null }).client_id ?? null,
    event_type: 'view',
  })

  const locale = (gen.locale as string) ?? 'en'
  const zh = locale === 'zh'

  const typeLabel: Record<string, string> = {
    quote:   zh ? '报价单' : 'Quotation',
    invoice: zh ? 'Invoice' : 'Invoice',
    email:   zh ? '商务邮件' : 'Business Email',
  }

  const toolHref: Record<string, string> = {
    quote:   `/${locale}/tools/quote`,
    invoice: `/${locale}/tools/invoice`,
    email:   `/${locale}/tools/email`,
  }

  const output = gen.output_data as QuoteOutput | InvoiceOutput | EmailOutput
  const validUntil = gen.tool_type === 'quote' ? (output as QuoteOutput).validUntil : undefined

  return (
    <div className="min-h-screen bg-slate-50">
      {/* Top navigation bar */}
      <header className="sticky top-0 z-10 bg-white/90 backdrop-blur border-b border-slate-200">
        <div className="max-w-4xl mx-auto px-4 sm:px-6 py-3 flex items-center justify-between gap-4">
          <div className="flex items-center gap-2.5">
            <div className="flex h-7 w-7 shrink-0 items-center justify-center rounded-md bg-indigo-600 text-white font-bold text-sm">B</div>
            <div className="hidden sm:block">
              <p className="text-[10px] text-slate-400 uppercase tracking-widest leading-none">{zh ? '企文助手' : 'BizDoc AI'}</p>
              <p className="text-sm font-semibold text-slate-700 leading-tight">{typeLabel[gen.tool_type] ?? gen.tool_type}</p>
            </div>
          </div>
          <ViewActions id={id} zh={zh} toolHref={toolHref[gen.tool_type] ?? `/${locale}`} zh_label={zh} />
        </div>
      </header>

      <main className="max-w-4xl mx-auto px-4 sm:px-6 py-8 space-y-5">
        {/* Validity warning */}
        {validUntil && <ValidityBanner validUntil={validUntil} zh={zh} />}

        {/* Document card */}
        <div className="relative bg-white rounded-2xl shadow-md overflow-hidden">
          {/* Top accent bar */}
          <div className="h-1.5 bg-gradient-to-r from-indigo-500 via-indigo-600 to-violet-600" />

          <div className="px-8 sm:px-12 py-10">
            {gen.tool_type === 'quote'   && <QuoteView   data={output as QuoteOutput}   company={company} zh={zh} />}
            {gen.tool_type === 'invoice' && <InvoiceView data={output as InvoiceOutput} company={company} invoiceStatus={gen.invoice_status} zh={zh} />}
            {gen.tool_type === 'email'   && <EmailView   data={output as EmailOutput}   zh={zh} />}
          </div>

          {/* Footer inside card */}
          <div className="px-8 sm:px-12 py-4 bg-slate-50/60 border-t border-slate-100 flex items-center justify-between gap-4">
            <p className="text-xs text-slate-400">
              {zh ? '此文书由企文助手 AI 生成' : 'Generated with BizDoc AI'}
            </p>
            <p className="text-xs text-slate-300 font-mono hidden sm:block select-all break-all">
              {`${process.env.NEXT_PUBLIC_SITE_URL ?? ''}/view/${id}`}
            </p>
          </div>
        </div>

        {/* Accept quote */}
        {gen.tool_type === 'quote' && !gen.accepted_at && (
          <QuoteAcceptButton genId={id} zh={zh} />
        )}
        {gen.tool_type === 'quote' && gen.accepted_at && (
          <div className="rounded-xl border border-emerald-200 bg-emerald-50 px-5 py-4 text-center space-y-1">
            <p className="text-emerald-700 font-semibold">✅ {zh ? '此报价单已被接受' : 'Quote Accepted'}</p>
            <p className="text-sm text-emerald-600">
              {zh ? `接受时间：${new Date(gen.accepted_at as string).toLocaleDateString('zh-CN')}` : `Accepted on ${new Date(gen.accepted_at as string).toLocaleDateString('en-US')}`}
            </p>
          </div>
        )}

        {/* CTA card */}
        <div className="rounded-2xl border border-indigo-100 bg-white px-6 py-5 flex flex-col sm:flex-row items-center justify-between gap-4 shadow-sm">
          <div>
            <p className="font-semibold text-slate-800 text-sm">{zh ? '也想生成这样的专业文书？' : 'Need a professional document like this?'}</p>
            <p className="text-xs text-slate-400 mt-0.5">{zh ? '免费使用，无需信用卡' : 'Free to use — no credit card needed'}</p>
          </div>
          <a
            href={toolHref[gen.tool_type] ?? `/${locale}`}
            className="shrink-0 rounded-xl bg-indigo-600 px-5 py-2.5 text-sm font-semibold text-white hover:bg-indigo-700 transition-colors shadow-sm"
          >
            {zh ? '免费生成我的文书 →' : 'Create mine — Free →'}
          </a>
        </div>
      </main>
    </div>
  )
}
